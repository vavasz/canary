local bossaddoutfit = CreatureEvent("bossaddoutfit")

function bossaddoutfit.onDeath(creature, corpse, killer, mostDamage, unjustified, mostDamage_unjustified)
    if not creature then
        return
    end

    local bossName = creature:getName():lower()
    local bossOutfits = bosses[bossName] and bosses[bossName].outfits
    if not bossOutfits then
        return
    end

    local damageMap = creature:getMonster():getDamageMap()
    for key, _ in pairs(damageMap) do
        local player = Player(key)
        if player then
            if math.random(100) <= 99 then
                local randomOutfitIndex = math.random(#bossOutfits)
                local randomOutfit = bossOutfits[randomOutfitIndex].id
                local randomAddon = math.random(1, 2)
                player:addOutfit(randomOutfit)
                player:addOutfitAddon(randomOutfit, randomAddon)
                player:sendTextMessage(MESSAGE_EVENT_ADVANCE, "Voce ganhou um outfit com addon " .. randomAddon .. "!")
            end
        end
    end
end

bossaddoutfit:register()








